package me.dueris.calio.util.holders;

public class QuadPair {
	public Object first;
	public Object second;
	public Object third;
	public Object fourth;

	public QuadPair(Object first, Object second, Object third, Object fourth) {
		this.first = first;
		this.second = second;
		this.third = third;
		this.fourth = fourth;
	}
}
